using UnityEngine;

public class CameraChase : MonoBehaviour
{
   
    [SerializeField] public Transform player;
    private Vector3 pos;

    private void Awake()
    {
        if (!player)
            player = FindObjectOfType<Hero>().transform;
    }

    private void Update()
    {
        pos = player.position;
        pos.z = -5f;

        transform.position = Vector3.Lerp(transform.position, pos, Time.deltaTime);
    }
}
